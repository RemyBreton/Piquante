const http = require("http"); // Importation du package de node
const app = require("./app"); // importation du fichier app.js

const normalizePort = (val) => {
  const port = parseInt(val, 10);

  if (isNaN(port)) {
    return val;
  }
  if (port >= 0) {
    return port;
  }
  return false;
};
const port = normalizePort(process.env.PORT || "3000"); // dira à l'application sur quel PORT tourner
app.set("port", port);

const errorHandler = (error) => {
  // gere de maniere appropriée les differentes erreurs
  if (error.syscall !== "listen") {
    throw error;
  }
  const address = server.address();
  const bind =
    typeof address === "string" ? "pipe " + address : "port: " + port;
  switch (error.code) {
    case "EACCES":
      console.error(bind + " requires elevated privileges.");
      process.exit(1);
      break;
    case "EADDRINUSE":
      console.error(bind + " is already in use.");
      process.exit(1);
      break;
    default:
      throw error;
  }
};

const server = http.createServer(app);
// Création du seveur en appellant la méthode créateServer du package Http
// qui prend la fonction qui sera appeler à chaque requete du serveur

server.on("error", errorHandler);
server.on("listening", () => {
  const address = server.address();
  const bind = typeof address === "string" ? "pipe " + address : "port " + port;
  console.log("Listening on " + bind);
});

server.listen(port); // Ecoute les requetes du port 3000
